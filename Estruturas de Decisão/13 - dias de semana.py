num_semana = int(input('Digite o número referente ao dia da semana (Domingo - 1 ...): '))
if num_semana == 1:
    print(f'Você digitou {num_semana}, o numero referente a Domingo!')
elif num_semana == 2:
    print(f'Você digitou {num_semana}, o numero referente a Segunda-Feira!')
elif num_semana == 3:
    print(f'Você digitou {num_semana}, o numero referente a Terça-Feira!')
elif num_semana == 4:
    print(f'Você digitou {num_semana}, o numero referente a Quarta-Feira!')
elif num_semana == 5:
    print(f'Você digitou {num_semana}, o numero referente a Quinta-Feira!')
elif num_semana == 6:
    print(f'Você digitou {num_semana}, o numero referente a Sexta-Feira!')
elif num_semana == 7:
    print(f'Você digitou {num_semana}, o numero referente a Sábado!')
else:
    print('Dia Inválido!')
